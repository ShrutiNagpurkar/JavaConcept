package nestedpackage;

public class Operators {

	public static void main(String[] args) {
		int i = +10;
		int i1 = -10;
		int i2 =  ++i; //11
		int i3 = --i; //10
		int i4 = i++; //10
		int i5 = i--; //11
		
		System.out.println(i);
		System.out.println(i1 + " " + i2 + " " + i3 + " " + i4 + " " + i5);

	}

}
